"""Mock Multipass tool outputs for the war room demo.

The agents speak to these via a text protocol:
    >>TOOL: multipass exec vm-web-01 -- journalctl -u nginx -n 20

The orchestrator parses that line, calls run_tool(), and injects the
output back into the transcript so the next agent turn sees it.

State note: the world has ONE bit of state — has vm-web-01 been reverted?
Before revert, reload/restart on vm-web-01 fails. After revert, it succeeds.
Other commands are stateless.
"""

import re
from dataclasses import dataclass


@dataclass
class WorldState:
    """The mocked world — tracks whether vm-web-01 has been reverted."""
    vm_web_01_reverted: bool = False


WORLD = WorldState()


# ----- Per-VM canned outputs ------------------------------------------------


