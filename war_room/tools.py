def _systemctl_nginx(vm: str, action: str) -> str:
    if vm == "vm-web-01" and not WORLD.vm_web_01_reverted:
        return f"""Job for nginx.service failed because the control process exited with error code.
See "systemctl status nginx.service" and "journalctl -xeu nginx.service" for details.
[FAILED] {action} nginx.service"""
    return f"[OK] {action} nginx.service"


# ----- Dispatch -------------------------------------------------------------


_JOURNALCTL_NGINX_RE = re.compile(r"^journalctl\b.*\bnginx\b", re.IGNORECASE)


_NESTED_EXEC_RE = re.compile(r"^multipass\s+exec\s+\S+\s+--\s+", re.IGNORECASE)


def multipass_exec(vm: str, cmd: str) -> str:
    """Dispatch a `multipass exec <vm> -- <cmd>` call to a mock implementation."""
    cmd = cmd.strip()
    # Defensive: models sometimes emit `multipass exec vm -- multipass exec vm -- cmd`.
    # Strip the nested prefix(es) and keep the inner command.
    while _NESTED_EXEC_RE.match(cmd):
        cmd = _NESTED_EXEC_RE.sub("", cmd, count=1).strip()

    if _JOURNALCTL_NGINX_RE.search(cmd):
        return _journalctl_nginx(vm)
    if cmd == "nginx -t" or cmd.startswith("sudo nginx -t"):
        return _nginx_test(vm)
    if cmd.startswith("cat /etc/nginx") or "nginx -T" in cmd:
        return _nginx_config_dump(vm)
    if "config-history" in cmd or cmd.startswith("git log"):
        return _config_history(vm)
    if "config-diff" in cmd or "git diff" in cmd:
        return _config_diff()
    if cmd.startswith("agent-log"):
        parts = cmd.split(maxsplit=1)
        agent_name = parts[1] if len(parts) > 1 else "deploy-agent"
        return _agent_log(agent_name)
    if "config-revert" in cmd or ("git revert" in cmd) or ("config revert" in cmd):
        return _config_revert(vm)
    if cmd.startswith("systemctl reload nginx"):
        return _systemctl_nginx(vm, "reload")
    if cmd.startswith("systemctl restart nginx"):
        return _systemctl_nginx(vm, "restart")

    return f"[mock] command not implemented in simulation: {cmd!r}"


def multipass(cmd: str) -> str:
    """Top-level `multipass <subcommand>` dispatcher."""
    cmd = cmd.strip()
    if cmd == "list" or cmd.startswith("list"):
        return _multipass_list()

    # `multipass exec <vm> -- <cmd...>`
    m = re.match(r"exec\s+(\S+)\s+--\s+(.+)", cmd, re.DOTALL)
    if m:
        return multipass_exec(m.group(1), m.group(2))

    return f"[mock] unsupported multipass subcommand: {cmd!r}"


# ----- Protocol parser ------------------------------------------------------

_TOOL_LINE_RE = re.compile(r"^>>TOOL:\s*(.+?)\s*$", re.MULTILINE)


def parse_tool_calls(text: str) -> list[str]:
    """Extract all `>>TOOL: <cmd>` invocations from an agent response."""
    return _TOOL_LINE_RE.findall(text)


# Tool calls that mutate state — these require human approval before running.
# Read-only tools (journalctl, nginx -t, cat, config-history, config-diff) run freely.
_MUTATING_PATTERNS = (
    "config-revert",
    "git revert",
    "systemctl reload",
    "systemctl restart",
    "sed -i",
)


def is_mutating(tool_line: str) -> bool:
    """True if this tool call changes system state and requires approval."""
    return any(p in tool_line for p in _MUTATING_PATTERNS)


# ────────────────────────────────────────────────────────────────────────
# INTEGRATION POINT: tool execution
#
# Every `_*` helper above returns a canned string. In production, swap
# each one for a call against your real infrastructure. The dispatcher
# (`run_tool`) and the wire protocol (`>>TOOL: ...`) stay identical —
# only the backing functions change. Examples:
#   - Real Multipass:  subprocess.run(["multipass", "exec", vm, "--", cmd])
#   - Juju:            subprocess.run(["juju", "run", "--unit", unit, cmd])
#   - Ansible ad-hoc:  ansible_runner.run(host=vm, module="shell", args=cmd)
#   - SSH with audit:  paramiko.SSHClient().exec_command(cmd)
# Mutating tools (config-revert, systemctl reload/restart) gate through
# `is_mutating()` — keep that contract so the human approval boundary in
# app.py still fires before any real side effect.
# ────────────────────────────────────────────────────────────────────────


def run_tool(tool_line: str) -> str:
    """Run a single tool invocation string and return its output.

    Supported forms:
        multipass list
        multipass exec <vm> -- <cmd...>
    """
    tool_line = tool_line.strip()
    if tool_line.startswith("multipass"):
        return multipass(tool_line[len("multipass"):].strip())
    return f"[mock] unknown tool: {tool_line!r}"


def reset_world():
    """Reset world state — call between demo runs."""
    WORLD.vm_web_01_reverted = False
